    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextFieldValorInvestimento.getText().equals("")) {

                JOptionPane.showMessageDialog(null, "Preencha o campo Valor Investimento!");
            } else if (jTextFieldPatrimonioDesejado.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencha o campo Patrim√¥nio desejado!");

            } else if (jTextFieldTempoInvestimento.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencha o campo Tempo de investimento!");

            } else if (jTextFieldIp.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencha o campo Tempo IP!");

            } else if (jTextFieldPorta.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencha o campo PORTA!");

            } else {

                Double valorInvestimento = Double.parseDouble(jTextFieldValorInvestimento.getText());
                Double patrimonioDesejado = Double.parseDouble(jTextFieldPatrimonioDesejado.getText());

                int tempoInvestimento = Integer.parseInt(jTextFieldTempoInvestimento.getText());
                String ip = jTextFieldIp.getText();
                int porta = Integer.parseInt(jTextFieldPorta.getText());

                double taxaJurosMensais = new ClienteSocket().patrimonio(valorInvestimento, patrimonioDesejado, tempoInvestimento, ip, porta);

                String resultado = String.format("%.5f", taxaJurosMensais);
                jTextFieldTaxaJuros.setText(String.valueOf(resultado));
            }

        } catch (NumberFormatException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButtonCalcularActionPerformed

